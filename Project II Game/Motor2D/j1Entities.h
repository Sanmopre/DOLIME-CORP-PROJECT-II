#ifndef __j1Entities_H__
#define __j1Entities_H__

#include "p2Point.h"
#include "p2Log.h"
#include "j1App.h"

struct SDL_Texture;

enum class Orientation
{
	NORTH,
	NORTH_EAST,
	EAST,
	SOUTH_EAST,
	SOUTH,
	SOUTH_WEST,
	WEST,
	NORTH_WEST
};

enum class Entity_Type
{
	BOAT,
	NONE
};
///////////////////////////////////////////////CLASSES//////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Entity
{
public:

	virtual void Update(float dt) {};
	virtual void CleanUp() {};

	fPoint position;
	Entity_Type type;
	bool selected;
	int level;

	SDL_Texture* texture;
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Unit : public Entity
{
public:

	int health;
	int speed;
	int range;
	Orientation looking;
	fPoint destination;

protected:

	fPoint past_frame_dest;
	//void Path_to(fPoint); Add when pathfinding is done
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Structure : public Entity
{
public:

	int health;

};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Resource : public Entity
{
public:

	int amount;

};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Boat : public Unit
{
public:
	Boat();
	Boat(float x, float y, int level);
	~Boat();

	void Update(float);
	void CleanUp();
};

#endif // __j1Entities_H__